def pick_lesson(decisions: list[dict], today: date) -> dict:
    """優先挑今天實際觸發的規則對應的課;否則按日期輪播。"""
    triggered: list[str] = []
    for d in decisions:
        triggered += [r["rule"] for r in d.get("reasons", [])]
        if d.get("risk_notes"):
            triggered.append("停損" if d["action"] == "SELL" else "部位集中度警示")
        triggered.append(d.get("action", ""))
    for lesson in sorted(LESSONS, key=lambda x: x["id"]):
        if any(tag in triggered for tag in lesson["tags"]):
            # 用日期讓同一批觸發規則不會永遠選同一課
            matched = [ls for ls in LESSONS if any(t in triggered for t in ls["tags"])]
            return matched[today.toordinal() % len(matched)]
    return LESSONS[today.toordinal() % len(LESSONS)]
